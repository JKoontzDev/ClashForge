def seed_benchmark_fighters(*, purge_missing: bool = False) -> dict:
    created = 0
    updated = 0

    benchmark_names = [payload['name'] for payload in BENCHMARK_FIGHTERS]
    for payload in BENCHMARK_FIGHTERS:
        _, was_created = Character.objects.update_or_create(
            name=payload['name'],
            defaults=payload,
        )
        if was_created:
            created += 1
        else:
            updated += 1

    deleted = 0
    if purge_missing:
        deleted, _ = Character.objects.filter(is_benchmark=True).exclude(
            name__in=benchmark_names
        ).delete()

    return {
        'created': created,
        'updated': updated,
        'deleted': deleted,
        'total': len(BENCHMARK_FIGHTERS),
    }
